'''
De las tuplas de coordenadas
(x1,y1)=(−20,−20) y (x2,y2)=(40,20)

Encuentra las coordenadas del punto medio, almacena en una tupla e imprime el resultado

(x,y)
'''

#Coordenadas
(x1, y1) = (-20, -20)
(x2, y2) = (40, 20)

#Punto medio
xm = (x1 + x2) / 2
ym = (y1 + y2) / 2

#Tupla del punto medio
tm = (xm, ym)

#Impresión
print(f"La tupla del punto medio es: {tm}")
print(f"Eso significa que xm es: {xm} y ym es: {ym}")